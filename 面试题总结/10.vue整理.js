/* 
  Vue：是一套用于构建用户界面的渐进式框架。
  ------------------------------------------------------------------------------------------------
  Vue的8个生命周期：
    beforeCreate：实例组件刚创建，元素DOM和数据都还没有初始化，暂时不知道能在这个周期里面进行生命操作。
    ----------------------------------------------------------------------------------------------
    created：数据data已经初始化完成，方法也已经可以调用，但是DOM未渲染。
    ----------------------------------------------------------------------------------------------
    beforeMount：DOM未完成挂载，数据也初始化完成，但是数据的双向绑定还是显示{{}}，
                 这是因为Vue采用了虚拟Dom技术，先占住了一个坑。
    ----------------------------------------------------------------------------------------------
    mounted：数据和DOM都完成挂载，在上一个周期占位的数据把值给渲染进去。
    ----------------------------------------------------------------------------------------------
    beforeUpdate：只要是页面数据改变了都会触发，数据更新之前，页面数据还是原来的数据，
                  当你请求赋值一个数据的时候会执行这个周期，如果没有数据改变不执行。
    ----------------------------------------------------------------------------------------------
    updated：只要是页面数据改变了都会触发，数据更新完毕，页面的数据是更新完成的。
             beforeUpdate和updated要谨慎使用，因为页面更新数据的时候都会触发，
             在这里操作数据很影响性能和容易死循环。
    ----------------------------------------------------------------------------------------------
    beforeDestroy：这个周期是在组件销毁之前执行，这个周期里面还可以使用data和method。
                   比如一个倒计时组件，如果在路由跳转的时候没有清除，这个定时器还是在的，
                   这时候就可以在这个里面清除计时器。
    ----------------------------------------------------------------------------------------------
    destroyed：Vue实例销毁后调用。调用后，Vue实例指示的所有东西都会解绑定，所有的事件监听器会被移除，
               所有的子实例也会被销毁。 
*/